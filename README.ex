Overview
--------

This charm provides genghisapp from http://genghisapp.com/

Genghis is a single file MongoDB Admin app. The application itself provides a dynamic on the fly connection to your MongoDB server/shard/replicaset defined by the user. All connection details are stored in browser local storage. 

Usage
-----

Step by step instructions on using the charm:

    juju deploy genghisapp


You can then browse to http://ip-address to configure the service. 

Configuration
-------------

Port - define the port Genghisapp will receive connections on.

